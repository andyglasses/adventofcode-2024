function forEachNeighbor(x, y, xLength, yLength, notStack, action){
    var coOrds = [];
    if(y > 0){
        if(x > 0){
            coOrds.push({ x: x -1, y: y -1 });
        }
        coOrds.push({ x: x, y: y -1 });
        if(x < xLength - 1){
            coOrds.push({ x: x + 1, y: y - 1 });
        }
    }
    if(x > 0){
        coOrds.push({ x: x -1, y: y });
    }
    if(x < xLength - 1){
        coOrds.push({ x: x +1, y: y });
    }
    if(y < yLength - 1){
        if(x > 0){
            coOrds.push({ x: x -1, y: y  +1 });
        }
        coOrds.push({ x: x, y: y +1 });
        if(x < xLength - 1){
            coOrds.push({ x: x + 1, y: y + 1 });
        }
    }
    coOrds = coOrds.filter(c => !notStack.some(s => s.x == c.x && s.y == c.y))
    coOrds.forEach(c => {
        action(c.x, c.y, [...notStack])
    });
    
}


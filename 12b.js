function run(){
  const input = getInput().trim().split('\n').map(function(x) { return x.trim().split(''); });
  const types = [];
  input.forEach(row => {
    row.forEach(i => {
      if(!types.some(t => t==i)){
types.push(i);
      }
    });
  });
  const plots = [];
  let plotId = 1;
  for(var y = 0; y < input.length; y++){
    for(var x= 0; x < input[0].length; x++){
      if(plots.some(p => p.points.some(i => i.x == x && i.y == y))){
        continue;
      }
      points = [{ x, y }];
      walkPoints(input, points, input[y][x], y, x);
      plots.push({ type: input[y][x], points: points, perimeter: 0, walls: [], id: plotId });
      plotId++;
    }
  }
  plots.forEach(p => {
    p.points.forEach(pp => {
      p.perimeter += walkPlot(input, p.type, pp.y, pp.x, p.walls, plots)
    })

    p.walls = collapseWalls(p.walls);
  });
  plots.forEach(p => console.log(p.id, p.type, p.points.length, p.walls.length, p.points.length * p.walls.length))
  var cost = plots.reduce((p, c) => {
    return p + (c.points.length * c.walls.length);
  }, 0)
  console.log(cost);
}

function collapseWalls(walls){
  const newWalls = [];
  const upDownWalls = walls.filter(w => w.from.x == w.to.x).sort((a,b) => a.from.y - b.from.y);
  upDownWalls.forEach(w => {
    if(w.used){
      return;
    }
    const sameCol = upDownWalls.filter(u => u.from.x == w.from.x && u.from.y != w.from.y && w.against == u.against);
    let newWall = {...w};
    w.used = true;
    let added = true
    while(added){
      added = false;
      // above
      const above = sameCol.find(u => u.to.y == newWall.from.y);
      if(above){
        above.used = true;
        newWall.from.y = above.from.y;
        added = true;
      }
      // below
      const below = sameCol.find(u => u.from.y == newWall.to.y);
      if(below){
        below.used = true;
        newWall.to.y = below.to.y;
        added = true;
      }
    }
    newWalls.push(newWall);
  });

  const leftRightWalls = walls.filter(w => w.from.y == w.to.y).sort((a,b) => a.from.x - b.from.x);
  leftRightWalls.forEach(w => {
    if(w.used){
      return;
    }
    const sameRow = leftRightWalls.filter(u => u.from.y == w.from.y && u.from.x != w.from.x && w.against == u.against);
    let newWall = {...w};
    w.used = true;
    let added = true
    while(added){
      added = false;
      // above
      const above = sameRow.find(u => u.to.x == newWall.from.x);
      if(above){
        above.used = true;
        newWall.from.x = above.from.x;
        added = true;
      }
      // below
      const below = sameRow.find(u => u.from.x == newWall.to.x);
      if(below){
        below.used = true;
        newWall.to.x = below.to.x;
        added = true;
      }
    }
    newWalls.push(newWall);
  });
  console.log(newWalls.map(w => `${w.from.y},${w.from.x} -> ${w.to.y},${w.to.x}`))
  return newWalls;
}

function walkPlot(grid, val, y, x, walls, plots){
  let count = 0;
  if(y > 0 && !isValid(grid, [], val, y-1, x)){
    count++
    let against = plots.find(p => p.points.some(z => z.x == x && z.y == y -1))
    walls.push({ from: { y: y-1, x: x -1 }, to: { y: y-1, x }, used: false, against: against.id })
  } else if(y == 0) {
    count++;
    walls.push({ from: { y: y-1, x: x -1 }, to: { y: y-1, x }, used: false, against: 0 })
  }
  if(y +1 < grid.length && !isValid(grid, [], val, y +1, x)){
    count++
    let against = plots.find(p => p.points.some(z => z.x == x && z.y == y +1))
    walls.push({ from: { y: y, x: x-1 }, to: { y: y, x }, used: false, against: against.id })
  } else if(y +1 == grid.length){
    count++;
    walls.push({ from: { y: y, x: x-1 }, to: { y: y, x }, used: false, against: 0 })
  }
  if(x > 0 && !isValid(grid, [], val, y, x -1)){
    count++
    let against = plots.find(p => p.points.some(z => z.x == x - 1 && z.y == y))
    walls.push({ from: { y:  y - 1, x: x -1 }, to: { y, x: x - 1 }, used: false, against: against.id })
  }else if(x == 0) {
    count++;
    walls.push({ from: { y:  y - 1, x: x -1 }, to: { y, x: x - 1 }, used: false, against: 0 })
  }
  if(x+1 < grid[0].length && !isValid(grid, [], val, y, x+1)){
    count++
    let against = plots.find(p => p.points.some(z => z.x == x + 1 && z.y == y))
    walls.push({ from: { y:  y - 1, x: x }, to: { y, x: x }, used: false, against: against.id })
  } else if(x +1 == grid[0].length){
    count++;
    walls.push({ from: { y:  y - 1, x: x }, to: { y, x: x }, used: false, against: 0 })
  }
  return count;
}


function walkPoints(grid, points, val, y, x){
  
  if(y > 0 && isValid(grid, points, val, y-1, x)){
    points.push({ x, y: y-1 })
    walkPoints(grid, points, val, y -1, x)
  } 
  if(y +1 < grid.length && isValid(grid, points, val, y +1, x)){
    points.push({ x, y: y+1 })
    walkPoints(grid, points, val, y +1, x)
  }
  if(x > 0 && isValid(grid, points, val, y, x -1)){
    points.push({ x: x - 1, y })
    walkPoints(grid, points, val, y, x- 1)
  }
  if(x+1 < grid[0].length && isValid(grid, points, val, y, x+1)){
    points.push({ x: x + 1, y })
    walkPoints(grid, points, val, y, x + 1)
  }
}

function isValid(grid, points, val, y, x){
  return grid[y][x] == val && !points.some(p => p.x ==x && p.y == y)
}

